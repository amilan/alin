# 	How to create images for ALIN project:
#
# 		1. Save the picture in png format
#		2. Open image with GIMP Linux Image Editor
#		3. Select image->Mode-> Gray Scale
#		4. Scale Picture (select proper image size, taking into account that display size is 128x64 pixels)
#		5. Export image to X bitmap image (*.bitmap) file. (Don't need to check any XBM option)
#		6. From the output file, copy the resulting file and add it to project.
#		7. ADD TWO BYTES to the beginning of the matrix, these are the width and heigh. They should be the two first bytes of the array
#
#		CAUTION: The function in code which is used to draw image, draws blocks of 8x8 pixels. Take this into account to calculate the image size.
#				i.e. if the image size is 56x18, it will paint from 0 to 55 and from 0 to 17, so the first two bytes of the array should be 0x37, 0x11,...

